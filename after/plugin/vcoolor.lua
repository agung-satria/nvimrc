vim.keymap.set("n", "<A-h>", ":VCoolor<cr>")
vim.keymap.set("i", "<A-h>", "<esc>:VCoolor<cr>")

-- ## Color Picker (VCoolor):
-- 
-- * `<Alt-H>` you can insert a hrx color anywhere (NORMAL and INSERT modes). (Yang diatas)
-- * `<Alt-R>` you can insert a rgb color anywhere (NORMAL and INSERT modes). (By default)
-- * `<Alt-V>` you can insert a hsl color anywhere (NORMAL and INSERT modes). (By default)
-- * `<Alt-W>` you can insert a rgba color anywhere (NORMAL and INSERT modes). (By default)
